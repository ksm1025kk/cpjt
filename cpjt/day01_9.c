#include<stdio.h>

int main8() {
	/*
	산술연산자
	+, -, *, /, %
	+ : 왼쪽값과 오른쪽값을 더하기
	- : 왼쪽값과 오른쪽값을 빼기
	* : 왼쪽값 곱하기 오른쪽값
	/ : 왼쪽값을 오른쪽값으로 나누기
	%% : 왼쪽값을 오른쪽값으로 나눈 나머지를 구해줌
	*/
	int num1 = 7, num2 = 3;
	printf("%d + %d = %d\n", num1, num2, num1 + num2);
	printf("%d - %d = %d\n", num1, num2, num1 - num2);
	printf("%d * %d = %d\n", num1, num2, num1 * num2);
	printf("%d / %d = %d\n", num1, num2, num1 / num2);
	printf("%d %% %d = %d\n", num1, num2, num1 % num2);





}